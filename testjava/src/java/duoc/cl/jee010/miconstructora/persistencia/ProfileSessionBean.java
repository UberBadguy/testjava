package duoc.cl.jee010.miconstructora.persistencia;

import duoc.cl.jee010.miconstructora.dto.ProfilesDTO;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.NoResultException;
import javax.persistence.NonUniqueResultException;
import javax.persistence.PersistenceContext;

/**
 *
 * @author cetecom
 */
@Stateless
public class ProfileSessionBean {
    @PersistenceContext
    private EntityManager em;
    
    public ProfilesDTO allProfiles() {
        ProfilesDTO profilesDTO = null;
        try {
            profilesDTO = em.createNamedQuery("Profile.findAll", ProfilesDTO.class)
                    .getSingleResult();
        } catch (NoResultException e) {
            return null;
        } catch (NonUniqueResultException e) {
            throw e;
        }
        return profilesDTO;
    }
}
